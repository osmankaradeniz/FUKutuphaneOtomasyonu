    private void kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydetActionPerformed

        try{
            String adsoyad = ad_soyad.getText().trim();
            String ogrno = ogrenci_no.getText().trim();
            String secilen_fakulte =  (String) fakulte.getSelectedItem();
            String secilen_bolum =  (String) bolum.getSelectedItem();
            
          
                if (!(ogrno.isBlank() || adsoyad.isBlank()) && !( (secilen_fakulte.equals("Seç")) || (secilen_bolum.equals("Seç"))) )  
                {
           String sql1 = "select bolum_id from bolumler where bolum_adi='"+secilen_bolum+"' and fakulte_id in (select fakulte_id from fakulteler where fakulte_adi='"+secilen_fakulte+"')";
           int bolum_id =-1;
           
           pst=conn.prepareStatement(sql1);
           rs=pst.executeQuery();
           
           while(rs.next())
           {
           bolum_id=rs.getInt("bolum_id");
           }
           pst.close();
           rs.close();
            
            
            String sql2 ="insert into ogrenciler(ogrenci_no,ogrenci_ad_soyad,bolum_id) values (?,?,'"+bolum_id+"')";
            
            String sql3 = "select * from ogrenciler where ogrenci_no='"+ogrno+"'";
            boolean kontrol_mevcutmu=false;
            // false'ü öğrencinin olmadığı bilgisi olarak kabul ediyorum.
                    
                    
                    
                pst=conn.prepareStatement(sql3);
                rs=pst.executeQuery();

                if(rs.next())
                {
                    JOptionPane.showMessageDialog(null, "Öğrenci Kaydı Başarısız ! -> Öğrenci No. Mevcut !");
                    kontrol_mevcutmu =true;
                }
                else
                { kontrol_mevcutmu =false; }
                
                if(kontrol_mevcutmu !=true )
                {
                    pst = conn.prepareStatement(sql2);
                    pst.setString(1,ogrno);
                    pst.setString(2,adsoyad);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Öğrenci Kayıt İşlemi Başarılı !");
                    pst.close();
                }
                rs.close();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Öğrenci Kaydı Başarısız ! -> Geçersiz bir değer girişi veya Boş Alan");
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"VERİ TABANI BAĞLANTISI BAŞARISIZ !: " + e.getSQLState());
        }

    }//GEN-LAST:event_kaydetActionPerformed

