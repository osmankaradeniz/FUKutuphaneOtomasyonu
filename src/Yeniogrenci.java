     void fakulte_getir() {
        
        fakulte.removeAllItems();
        fakulte.addItem("Seç");
        fakulte.setSelectedIndex(0);
        
        try {

            String sql = "select fakulte_adi from fakulteler";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                fakulte.addItem(rs.getString("fakulte_adi"));
            }
            pst.close();
            rs.close();
            

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI BAĞLANTISI BAŞARISIZ !: " + e.getSQLState());
        }
    }
     
    void bolum_getir(String fakulte_adi) {

        bolum.removeAllItems();

        bolum.addItem("Seç");

        bolum.setSelectedIndex(0);

        try {

            String sql = "select bolum_adi from bolumler where fakulte_id in (select fakulte_id from fakulteler where fakulte_adi='"+fakulte_adi+"')";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {

                bolum.addItem(rs.getString("bolum_adi"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI BAĞLANTISI BAŞARISIZ !: " + e.getSQLState());
        }
    }
   
    
    
    
    
    
   
