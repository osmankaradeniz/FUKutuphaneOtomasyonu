    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed

        try {

            String baslik = yeni_baslik.getText().trim();
            String yazar = yeni_yazar.getText().trim();
            String tur = (String) yeni_tur.getSelectedItem();
            int tur_id = -1;

            String sql;

            sql = "select * from kitap_turleri where kitap_tur='" + tur + "'";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                tur_id = rs.getInt("kitap_turleri_id");
            }
            pst.close();
            rs.close();

            String isbn = yeni_isbn.getText().trim();

            sql = "select * from kitaplar where kitap_isbn='" + isbn + "' and kitap_id!='"+secilen_kitap_id+"'";
            boolean kontrol_mevcutmu = false;
            // false'ü kitap ısbn olmadığını ifade ettiğini farz edelim.

            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Bu ISBN'de bir kitap zaten mevcut ! , lütfen ISBN kodunu kontrol ediniz...");
                kontrol_mevcutmu = true;
            } else {
                kontrol_mevcutmu = false;
            }

            String baski_sayi = (String) yeni_baskisayi.getText();
            String baski_yil = yeni_baskiyil.getText().trim();
            String yayinevi = yeni_yayinevi.getText().trim();
            String sayfa_sayi = yeni_sayfasayi.getText().trim();
            String fiyat = yeni_fiyat.getText().trim();

            boolean kontrol_1 = genelfonksiyonlar.int_sayimi(yeni_baskisayi.getText());
            boolean kontrol_2 = genelfonksiyonlar.int_sayimi(yeni_baskiyil.getText());
            boolean kontrol_3 = genelfonksiyonlar.int_sayimi(yeni_sayfasayi.getText());
            boolean kontrol_4 = genelfonksiyonlar.double_sayimi(yeni_fiyat.getText());

            if (kontrol_mevcutmu != true) {
                if (!(baslik.isBlank() || yazar.isBlank() || isbn.isBlank() || yayinevi.isBlank()) && (kontrol_1 && kontrol_2 && kontrol_3 && kontrol_4) == true && !(tur.equals("Seç"))) {
                    sql = "update kitaplar set kitap_baslik= ? , kitap_yazar=? , kitap_turleri_id=? , kitap_isbn=? , kitap_baski_sayi=?, kitap_baski_yil=? , kitap_yayinevi=? , kitap_sayfa_sayi=? , kitap_fiyat=? where kitap_id='" + secilen_kitap_id + "'";
                    pst = conn.prepareStatement(sql);

                    pst.setString(1, baslik);
                    pst.setString(2, yazar);
                    pst.setInt(3, tur_id);
                    pst.setString(4, isbn);
                    pst.setString(5, baski_sayi);
                    pst.setString(6, baski_yil);
                    pst.setString(7, yayinevi);
                    pst.setString(8, sayfa_sayi);
                    pst.setString(9, fiyat);
                    pst.executeUpdate();
                    pst.close();
                    JOptionPane.showMessageDialog(null, "KİTAP GÜNCELLENDİ !");
                    tablo_veri_doldur();
                } else {
                    JOptionPane.showMessageDialog(null, "Geçersiz bir format girişi veya Boş alan !");

                }
            } 
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI HATA !: " + e.getSQLState());
            e.printStackTrace();
        }


    }//GEN-LAST:event_guncelleActionPerformed

    private void veritableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_veritableMouseReleased

        DefaultTableModel model = (DefaultTableModel) veritable.getModel();

        int secilen_satir_indis = veritable.getSelectedRow();

        secilen_kitap_id = Integer.parseInt(model.getValueAt(secilen_satir_indis, 0).toString());
        String tur = model.getValueAt(secilen_satir_indis, 3).toString();

        kitap_id.setText("► SEÇİLEN KİTAP ID : " + secilen_kitap_id + " ◄");
        kitapbilgiler_ackapa(true);
        yeni_baslik.setText(model.getValueAt(secilen_satir_indis, 1).toString());
        yeni_yazar.setText(model.getValueAt(secilen_satir_indis, 2).toString());

        yeni_tur.setSelectedItem(tur);

        yeni_isbn.setText(model.getValueAt(secilen_satir_indis, 4).toString());
        yeni_baskisayi.setText(model.getValueAt(secilen_satir_indis, 5).toString());
        yeni_baskiyil.setText(model.getValueAt(secilen_satir_indis, 6).toString());
        yeni_yayinevi.setText(model.getValueAt(secilen_satir_indis, 7).toString());
        yeni_sayfasayi.setText(model.getValueAt(secilen_satir_indis, 8).toString());;
        yeni_fiyat.setText(model.getValueAt(secilen_satir_indis, 9).toString());


    }//GEN-LAST:event_veritableMouseReleased

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed

        try {
            String sql = "delete from kitaplar where kitap_id=" + secilen_kitap_id + ";";
            pst = conn.prepareStatement(sql);

            int deger = JOptionPane.showConfirmDialog(null, "İD : " + secilen_kitap_id + " olan kitabı silme işlemine devam etmek istiyor musunuz ?");

            if (deger == 0) {
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "KİTAP SİLİNDİ !");
                tazele();
            } else {
                JOptionPane.showMessageDialog(null, "İŞLEM İPTAL EDİLDİ !");
            }

            pst.close();
            tablo_veri_doldur();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI HATA !: " + e.getSQLState());
        }

    }//GEN-LAST:event_silActionPerformed

