    public void tablo_veri_doldur() {

        veritable.setDefaultEditor(Object.class, null);

        try {
         String sql = "SELECT kitap_id as '#ID' ,kitap_baslik as 'Başlık' ,kitap_yazar as 'Yazar' ,kitap_tur as 'Tür' ,kitap_isbn as 'ISBN' ,kitap_baski_sayi as 'Baskı Sayısı',kitap_baski_yil 'Baskı Yılı',kitap_yayinevi as 'Yayınevi',kitap_sayfa_sayi as 'Sayfa Sayısı',kitap_fiyat as 'Fiyat(TL)' from kitaplar AS kitap_turleri INNER JOIN kitap_turleri AS kitaplar on kitaplar.kitap_turleri_id=kitap_turleri.kitap_turleri_id";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            veritable.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI BAĞLANTISI BAŞARISIZ !: " + e.getSQLState());
        }

    }

