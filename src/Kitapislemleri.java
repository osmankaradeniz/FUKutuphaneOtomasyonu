    void kitap_turleri_getir() {
        tur_bul.removeAllItems();
        yeni_tur.removeAllItems();

        tur_bul.addItem("Seç");
        yeni_tur.addItem("Seç");

        tur_bul.setSelectedIndex(0);
        yeni_tur.setSelectedIndex(0);

        try {

            String sql = "select kitap_tur from kitap_turleri";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                tur_bul.addItem(rs.getString("kitap_tur"));
                yeni_tur.addItem(rs.getString("kitap_tur"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "VERİ TABANI BAĞLANTISI BAŞARISIZ !: " + e.getSQLState());
        }
    }

